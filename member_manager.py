"""
=============================================================
  member_manager.py — ไฟล์จัดการข้อมูลสมาชิก
  ทำหน้าที่: ลงทะเบียนสมาชิกใหม่ และค้นหาข้อมูลสมาชิกจากไฟล์
=============================================================
"""

# ----------------------------------------------------------
# นำเข้าไลบรารีที่ต้องใช้
# ----------------------------------------------------------
import os  # ใช้สำหรับจัดการไฟล์และโฟลเดอร์


# ----------------------------------------------------------
# ตัวแปร: กำหนด Path ของไฟล์สมาชิก
# ----------------------------------------------------------
# os.path.dirname(__file__) = โฟลเดอร์ที่ไฟล์นี้อยู่
# os.path.join(...)         = ต่อ path ให้ถูกต้องทุก OS
MEMBER_FILE = os.path.join(os.path.dirname(__file__), "data", "members.txt")


# ==============================================================
# ฟังก์ชัน: สร้างไฟล์สมาชิก (ถ้ายังไม่มี)
# ==============================================================
def ensure_file_exists():
    """
    ตรวจสอบว่าไฟล์ members.txt มีอยู่แล้วหรือยัง
    ถ้ายังไม่มี → สร้างโฟลเดอร์ data/ และสร้างไฟล์เปล่าขึ้นมา

    เรียกใช้ทุกครั้งก่อนอ่านหรือเขียนไฟล์ เพื่อป้องกัน Error
    """
    # สร้างโฟลเดอร์ data/ ถ้ายังไม่มี (exist_ok=True = ไม่ Error ถ้ามีแล้ว)
    os.makedirs(os.path.dirname(MEMBER_FILE), exist_ok=True)

    # ถ้าไฟล์ยังไม่มี → สร้างไฟล์ว่างเปล่า
    if not os.path.exists(MEMBER_FILE):
        open(MEMBER_FILE, "w", encoding="utf-8").close()


# ==============================================================
# ฟังก์ชัน: ลงทะเบียนสมาชิกใหม่
# ==============================================================
def register_member(phone, first_name, last_name):
    """
    ลงทะเบียนสมาชิกใหม่โดยบันทึกข้อมูลลงในไฟล์ members.txt
    จะตรวจสอบก่อนว่าเบอร์โทรนี้สมัครไว้แล้วหรือยัง

    พารามิเตอร์:
        phone      (str): เบอร์โทรศัพท์ (ใช้เป็น ID ของสมาชิก)
        first_name (str): ชื่อจริง
        last_name  (str): นามสกุล

    คืนค่า:
        (True,  ข้อความ) ถ้าลงทะเบียนสำเร็จ
        (False, ข้อความ) ถ้าเบอร์นี้สมัครไว้แล้ว
    """
    ensure_file_exists()  # ตรวจสอบว่าไฟล์มีอยู่ก่อน

    # --- ขั้นตอนที่ 1: ตรวจสอบว่าเบอร์โทรนี้ซ้ำกับที่มีอยู่ไหม ---
    with open(MEMBER_FILE, "r", encoding="utf-8") as f:
        for line in f:
            parts = line.strip().split(",")  # แยกแต่ละช่องด้วยคอมม่า

            # ถ้าบรรทัดนั้นมีข้อมูลครบ และเบอร์ตรงกัน → แจ้งว่าซ้ำ
            if len(parts) >= 3 and parts[0] == phone:
                return False, "เบอร์โทรศัพท์นี้เป็นสมาชิกอยู่แล้ว"

    # --- ขั้นตอนที่ 2: บันทึกสมาชิกใหม่ต่อท้ายไฟล์ ---
    with open(MEMBER_FILE, "a", encoding="utf-8") as f:
        # โหมด "a" (append) = เขียนต่อท้ายไฟล์ ไม่ลบของเดิม
        f.write(f"{phone},{first_name},{last_name}\n")

    return True, "สมัครสมาชิกสำเร็จ"


# ==============================================================
# ฟังก์ชัน: ค้นหาข้อมูลสมาชิกจากเบอร์โทร
# ==============================================================
def get_member(phone):
    """
    ค้นหาและดึงข้อมูลสมาชิกจากไฟล์ members.txt โดยใช้เบอร์โทรเป็นตัวค้น

    พารามิเตอร์:
        phone (str): เบอร์โทรศัพท์ที่ต้องการค้นหา

    คืนค่า:
        dict ข้อมูลสมาชิก {"phone", "first_name", "last_name"} ถ้าพบ
        None ถ้าไม่พบในระบบ
    """
    ensure_file_exists()  # ตรวจสอบว่าไฟล์มีอยู่ก่อน

    with open(MEMBER_FILE, "r", encoding="utf-8") as f:
        for line in f:
            parts = line.strip().split(",")  # แยกแต่ละช่องด้วยคอมม่า

            # ถ้าบรรทัดนั้นมีข้อมูลครบ และเบอร์ตรงกัน → คืนข้อมูลสมาชิก
            if len(parts) >= 3 and parts[0] == phone:
                return {
                    "phone":      parts[0],  # เบอร์โทร
                    "first_name": parts[1],  # ชื่อจริง
                    "last_name":  parts[2],  # นามสกุล
                }

    return None  # ค้นจบทั้งไฟล์แล้วไม่พบ → คืนค่า None
